package operators;

public class comparison {

	public static void main(String[] args) {
		
		// Comparison Operators
		int ahmedAge = 20;
		int mohamedAge = 15;
		// greater than
		System.out.println(ahmedAge > mohamedAge);
		// less than
		System.out.println(ahmedAge < mohamedAge);
		// equal
		System.out.println(ahmedAge == mohamedAge);
		// not equal
		System.out.println(ahmedAge != mohamedAge);
		// greater than or equal
		System.out.println(ahmedAge >= mohamedAge);
		// less than or equal
		System.out.println(ahmedAge <= mohamedAge);
	}
}
